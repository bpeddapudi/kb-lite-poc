/**
 * Traces public/Equinix-Emblem.png with potrace and writes SVG + extracted path(s).
 * Run: node scripts/trace-equinix-emblem.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import potrace from 'potrace'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const pngPath = path.join(root, 'public/Equinix-Emblem.png')
const outSvg = path.join(root, 'src/assets/equinix-emblem-traced.svg')
const outPathModule = path.join(root, 'src/components/visuals/equinixEmblemPath.generated.js')

potrace.trace(
  pngPath,
  {
    threshold: potrace.Potrace.THRESHOLD_AUTO,
    turdSize: 2,
    optTolerance: 0.2,
    color: 'none',
    background: 'transparent',
  },
  (err, svg) => {
    if (err) {
      console.error(err)
      process.exit(1)
    }
    fs.mkdirSync(path.dirname(outSvg), { recursive: true })
    fs.writeFileSync(outSvg, svg)

    const paths = [...svg.matchAll(/<path[^>]*\sd="([^"]+)"/g)].map((m) => m[1])
    console.log('SVG written:', outSvg)
    console.log('path count:', paths.length)

    if (paths.length === 0) {
      console.error('No <path d="..."> found in traced SVG')
      process.exit(1)
    }

    // Use longest path (main emblem outline)
    const main = paths.reduce((a, b) => (a.length >= b.length ? a : b))

    const viewBoxMatch = svg.match(/viewBox="([^"]+)"/)
    const viewBox = viewBoxMatch ? viewBoxMatch[1] : '0 0 100 100'

    const moduleSrc = `/* eslint-disable max-len */
/* Generated by scripts/trace-equinix-emblem.mjs from public/Equinix-Emblem.png — do not edit by hand. */
export const EQUINIX_EMBLEM_PATH = ${JSON.stringify(main)}
export const EQUINIX_EMBLEM_VIEWBOX = ${JSON.stringify(viewBox)}
`

    fs.writeFileSync(outPathModule, moduleSrc)
    console.log('Path module written:', outPathModule)
    console.log('viewBox:', viewBox)
    console.log('path length (chars):', main.length)
  },
)
